def dict_key(k):
    k = str(k).replace('.', '_')
    return k


def contribute_to_class(dct):
    def decorated(cls):
        for k, v in dct.iteritems():
            if not k:
                continue
            else:
                setattr(cls, dict_key(k), v)
        return cls
    return decorated


def contribute_to_class_by_value(dct):
    def decorated(cls):
        for k, v in dct.iteritems():
            if not v:
                continue
            else:
                setattr(cls, dict_key(v), k)
        return cls
    return decorated


